    assert.ok(maxF(capped.gcode) < maxF(fast.gcode), 'volumetric cap lowers the top feedrate');
    assert.ok(maxF(capped.gcode) <= 3750 + 1, `capped feedrate ~<=3750, got ${maxF(capped.gcode)}`);
