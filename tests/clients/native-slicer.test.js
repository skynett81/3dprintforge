describe('native-slicer: sliceLayer', () => {
  it('a 10mm cube cut at z=5 yields one square polygon', () => {
    const cube = box(10, 10, 10);
    const polys = sliceLayer(cube, 5);
    assert.ok(polys.length >= 1, 'at least one polygon');
    // Square outline — 4 corners + up to 4 midpoint vertices from
    // diagonal edges of split-quad faces (8 total). Still a square.
    const total = polys[0].length;
    assert.ok(total >= 4 && total <= 12, `expected 4-12 vertices, got ${total}`);
    // Verify it spans the full 10×10 outline.
    const xs = polys[0].map(p => p[0]);
    const ys = polys[0].map(p => p[1]);
    assert.equal(Math.min(...xs), 0);
    assert.equal(Math.max(...xs), 10);
    assert.equal(Math.min(...ys), 0);
    assert.equal(Math.max(...ys), 10);
  });

  it('cube cut above its top z returns empty', () => {
    const cube = box(10, 10, 10);
    const polys = sliceLayer(cube, 12);
    assert.equal(polys.length, 0);
  });

  it('cube cut below its bottom z returns empty', () => {
    const cube = box(10, 10, 10);
    const polys = sliceLayer(cube, -1);
    assert.equal(polys.length, 0);
  });

  it('cylinder cut at mid-height yields a many-vertex polygon', () => {
    const cyl = cylinder(10, 20, 32);
    const polys = sliceLayer(cyl, 10);
    assert.ok(polys.length >= 1);
    assert.ok(polys[0].length > 16, `cylinder polygon should have many vertices, got ${polys[0].length}`);
  });
});

describe('native-slicer: offsetPolygon', () => {
  it('shrinking a square by 1 produces a smaller square', () => {
    const square = [[0, 0], [10, 0], [10, 10], [0, 10]];
    const inner = offsetPolygon(square, -1);
    assert.equal(inner.length, 4);
    // Each corner moves inward by ~1 unit along the bisector.
    const minX = Math.min(...inner.map(p => p[0]));
    const maxX = Math.max(...inner.map(p => p[0]));
    assert.ok(Math.abs(minX - 1) < 0.1, `inner left should be ~1, got ${minX}`);
    assert.ok(Math.abs(maxX - 9) < 0.1, `inner right should be ~9, got ${maxX}`);
  });

  it('growing a square by 1 produces a larger square', () => {
    const square = [[0, 0], [10, 0], [10, 10], [0, 10]];
    const outer = offsetPolygon(square, 1);
    assert.equal(outer.length, 4);
    const minX = Math.min(...outer.map(p => p[0]));
    const maxX = Math.max(...outer.map(p => p[0]));
    assert.ok(minX < -0.5 && maxX > 10.5);
  });

  it('returns empty for degenerate (over-shrunk) polygon', () => {
    const tinySquare = [[0, 0], [2, 0], [2, 2], [0, 2]];
    const inner = offsetPolygon(tinySquare, -10);
    assert.deepEqual(inner, []);
  });
});

